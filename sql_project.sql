-- 14.	Display the top 3 restaurants in each city based on total orders received. 
SELECT *  FROM 
( SELECT R.city, R.restaurant_name,
		 COUNT(O.order_id) AS total_orders,
		 DENSE_RANK() OVER(PARTITION BY R.city ORDER BY COUNT(O.order_id) DESC) AS rnk
		 FROM restaurants R JOIN orders O
         ON R.restaurant_id = O.restaurant_id
         GROUP BY R.city , R.restaurant_name
) temp
WHERE rnk <= 3;

-- 15.	Calculate month-wise revenue and show the previous month revenue.
SELECT month_no, revenue,
       LAG(revenue) OVER(ORDER BY month_no) AS previous_month_revenue
       FROM ( SELECT MONTH(order_date) AS month_no,
			  SUM(order_amount) AS revenue
              FROM orders
              GROUP BY MONTH(order_date)
			) temp;

