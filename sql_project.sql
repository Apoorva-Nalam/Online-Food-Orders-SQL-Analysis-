-- 1.	Display all completed orders along with customer name and restaurant name. 
SELECT C.Customer_Id,  C.customer_name, R.restaurant_name ,O.Order_Status
FROM customers C JOIN orders O 
ON C.Customer_Id = O.Customer_Id
JOIN restaurants R 
ON O.Restaurant_Id = R.Restaurant_Id
WHERE Order_Status = "Completed";

-- 2.	Find the total revenue generated by each restaurant. 
SELECT R.restaurant_name , SUM(Order_Amount) AS Amount
FROM orders O JOIN restaurants R 
ON O.Restaurant_Id= R.Restaurant_Id
GROUP BY R.Restaurant_Name;

-- 3.	Show the total number of orders placed by each customer.
SELECT C.Customer_Id, C.customer_name, COUNT(O.order_id ) AS orders_placed
FROM customers C JOIN orders O 
ON C.customer_id = O.customer_id 
GROUP BY  C.Customer_Id, C.Customer_Name
order by orders_placed ;

-- 4.	Find the average order amount for each city.
SELECT R.city , AVG(Order_Amount) AS Amount
FROM orders O JOIN restaurants R 
ON O.Restaurant_Id= R.Restaurant_Id
GROUP BY R.city;

-- 5.	Categorize orders as Low, Medium, and High based on order amount.
SELECT Order_Amount,
 (  Case 
	WHEN Order_Amount >= 1000 THEN "High"
    WHEN Order_Amount >= 500  THEN "Medium"
    Else "Low"
    END ) AS Amount_Spending
 FROM orders
 Order by Order_Amount ,
	( CASE Amount_Spending
		WHEN "High" THEN 1
		WHEN "Medium" THEN 2
		WHEN "Low" THEN 3
		END ) ;

