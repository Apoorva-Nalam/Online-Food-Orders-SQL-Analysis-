-- 10.	Calculate the running total of revenue by order date. 
SELECT Order_Date, daily_revenue ,
SUM(daily_revenue) over(ORDER BY Order_Date) AS running_total
from 
( SELECT order_date, SUM(order_amount) AS daily_revenue
FROM orders
GROUP BY Order_Date
)temp ;

-- 11.	Rank restaurants based on total revenue generated. 
SELECT restaurant_name,
       total_revenue,
       DENSE_RANK() OVER (ORDER BY total_revenue DESC) AS ranks_restaurant
FROM (
    SELECT R.restaurant_name,
           SUM(O.Order_Amount) AS total_revenue
    FROM restaurants R
    JOIN orders O
    ON O.Restaurant_Id = R.Restaurant_Id
    GROUP BY R.restaurant_name
) temp;

